#include "libft.h"

void	ft_bzero(void *src, size_t n)
{
	int				i;
	unsigned char	*buf;

	buf = (unsigned char *) src;
	i = 0;
	while (i < n)
	{
		buf[i] = 0;
		i++;
	}
}
